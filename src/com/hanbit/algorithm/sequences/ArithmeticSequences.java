package com.hanbit.algorithm.sequences;
public class ArithmeticSequences{  
	public static void main(String[]args){ 
		
	for(int i=1;i<10;i++){
		for(int j=2;j<6;j++){
			System.out.print(j+"*"+i+"="+i*j+"\t");
					}
					System.out.print("\n");
	}	
    System.out.print("\n");
	
	for(int k=1;k<10;k++){
		for(int l=6;l<10;l++){
		System.out.print(l+"*"+k+"="+l*k+"\t");
		
		}
	    System.out.print("\n");
	}
	
    System.out.print("\n");
	}
	
}
















